package com.spring.model;

public class CandidateSkillsInfo {
	
	private int id;
	private String skillName;
	private String skillExperiencre;
	private String trainingType;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSkillName() {
		return skillName;
	}
	public void setSkillName(String skillName) {
		this.skillName = skillName;
	}
	public String getSkillExperiencre() {
		return skillExperiencre;
	}
	public void setSkillExperiencre(String skillExperiencre) {
		this.skillExperiencre = skillExperiencre;
	}
	public String getTrainingType() {
		return trainingType;
	}
	public void setTrainingType(String trainingType) {
		this.trainingType = trainingType;
	}
	
	

}
